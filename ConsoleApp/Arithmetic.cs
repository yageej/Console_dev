namespace ConsoleApp;

public class Arithmetic
{

    // Method inside this class 
    //doenst need static
    public int sumOfNumbers(int n1, int n2, int n3)
    {
        return n1 + n2 + n3;
    }

   public int DiffOfNumbers(int n1, int n2, int n3)
    {
        return n1 - n2 - n3;
    }
}
