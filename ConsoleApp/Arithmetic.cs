namespace ConsoleApp;

public static class Arithmetic         // can instantiate if using static modifier
{

    // Method inside this class 
    //doenst need static
    public static int sumOfNumbers(int n1, int n2, int n3)
    {
        return n1 + n2 + n3;
    }

   public static int DiffOfNumbers(int n1, int n2, int n3)
    {
        return n1 - n2 - n3;
    }
}
